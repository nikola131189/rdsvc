struct ScanCodeTranslator
{
	static uint32_t sdlToSystem(SDL_Scancode s)
	{
#ifdef _WIN32
		return sdl_scancode_windows_virtualcode_table[s];
#endif
		
#ifdef __linux__ 
		return sdl_scancode_X11_code_table[s];
#endif
	}

	static SDL_Scancode systemToSdl(uint32_t c)
	{
		for (size_t i = 0; i < 240; i++)
		{

#ifdef _WIN32
			if(sdl_scancode_windows_virtualcode_table[i] == c)
				return (SDL_Scancode)i;
#endif

#ifdef __linux__ 
			if (sdl_scancode_X11_code_table[i] == c)
				return (SDL_Scancode)i;
#endif
		}
		return (SDL_Scancode)0;
	}

};


